import { apiBaseUrl } from './constants';

Cypress.Commands.add('switchBackendPort', (port) => {
  cy.intercept({ url: `${apiBaseUrl}/**`, middleware: true }, (req) => {
    req.url = `http://localhost:${port}${req.url.slice(21)}`;
    req.continue();
  });
});
